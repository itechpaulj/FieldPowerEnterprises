    private void Customer_TableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_Customer_TableMouseClicked
        int i=Customer_Table.getSelectedRow();
        TableModel model = Customer_Table.getModel();
        
        cus_id = model.getValueAt(i,0).toString();
        name = model.getValueAt(i,1).toString();
        add = model.getValueAt(i,2).toString();
        contact = model.getValueAt(i,3).toString();
        
        String[] options = new String[3];
        options[0] = (" GET THIS CUSTOMER ");
        options[1] = (" UPDATE   CUSTOMER ");
        options[2] = (" DELETE   CUSTOMER ");
        
        int opt = JOptionPane.showOptionDialog(null," DO YOU WANT TO ",null, 0,JOptionPane.UNDEFINED_CONDITION,null,options,null);
        
        if(opt == 0)
        {
            AddCart.customer_name.setText(name);
            AddCart.address.setText(add);
            AddCart.customer_contact.setText(contact);
            AddCart.customer_id = cus_id;
            dispose();
        }
        else if(opt == 1)
        {
            if(Webpage.log.getText().equals("STAFF"))
            {
               
                JOptionPane.showMessageDialog(null,"ONLY ADMIN CAN ACCESS THIS ",null,JOptionPane.UNDEFINED_CONDITION);
            }
            else
            {
                Insert_Customer ic = new Insert_Customer();
                ic.setVisible(true);
                Insert_Customer.Display_Customer.setText("UPDATE CUSTOMER");
                ct.Customer();
            }
        }
        else if(opt == 2)
        {
            if(Webpage.log.getText().equals("STAFF"))
            {
               
                JOptionPane.showMessageDialog(null,"ONLY ADMIN CAN ACCESS THIS ",null,JOptionPane.UNDEFINED_CONDITION);
            }
            else
            {
          
            String[] deleteoptions = new String[2];
            deleteoptions[0] = (" YES ");
            deleteoptions[1] = (" NO  ");

            int opts = JOptionPane.showOptionDialog(null," DO YOU WANT TO DELETE ?",null, 0,JOptionPane.UNDEFINED_CONDITION,null,deleteoptions,null);

            if(opts == 0)
            {
                if(!Class_Customers.DeleteCustomer(cus_id)){        
                     JOptionPane.showMessageDialog(null, "DELETE SUCCESSFULLY",null,JOptionPane.INFORMATION_MESSAGE);
                     ct.Customer();
                     }
            }

            }
           
        }
    }//GEN-LAST:event_Customer_TableMouseClicked

